"""Join Kaggle performance rows to Transfermarkt IDs and latest valuations."""
import csv,json,re,sys,unicodedata
from pathlib import Path
stats_path,players_path,valuations_path=map(Path,sys.argv[1:4]); target=Path(__file__).resolve().parents[1]/"data.generated.js"
EUR_TO_GBP=.85 # Fixed prototype conversion; source values are EUR.
def number(row,key):
    try:return float(row.get(key) or 0)
    except (ValueError,AttributeError):return 0
def per90(row,key):
    n=number(row,"90s"); return round(number(row,key)/n,2) if n else 0
def norm(value):
    value=unicodedata.normalize("NFKD",value or "").encode("ascii","ignore").decode().lower()
    return re.sub(r"[^a-z0-9]","",value)
def position(raw):
    if "FW" in raw:return "Forward"
    if "MF" in raw:return "Midfielder"
    return "Defender"
identity={}
with players_path.open(encoding="utf-8-sig",newline="") as handle:
    for row in csv.DictReader(handle):identity.setdefault(norm(row["name"]),[]).append(row)
latest={}
with valuations_path.open(encoding="utf-8-sig",newline="") as handle:
    for row in csv.DictReader(handle):
        current=latest.get(row["player_id"])
        if not current or row["date"]>current["date"]:latest[row["player_id"]]=row
players=[]; matched=0
with stats_path.open(encoding="utf-8-sig",newline="") as handle:
    for row in csv.DictReader(handle):
        if number(row,"90s")<3 or "GK" in row["Pos"]:continue
        candidates=identity.get(norm(row["Player"]),[])
        tm=next((p for p in candidates if norm(p["current_club_name"])==norm(row["Squad"])),candidates[0] if len(candidates)==1 else None)
        valuation=latest.get(tm["player_id"]) if tm else None
        if not valuation:continue
        market_value=round(number(valuation,"market_value_in_eur")*EUR_TO_GBP/1_000_000,2)
        matched+=1
        age=int(number(row,"Age")); goals=per90(row,"Gls"); assists=per90(row,"Ast"); tackles=per90(row,"TklW"); interceptions=per90(row,"Int"); fouls=per90(row,"Fls"); role=position(row["Pos"]); seed=goals*4+assists*3
        players.append({"name":row["Player"],"club":row["Squad"],"country":row["Nation"].split()[-1] if row["Nation"] else "—","age":age,"position":role,"marketValue":market_value,"valuationDate":valuation["date"] if valuation else None,"goalsPer90":goals,"assistsPer90":assists,"xGPer90":round(goals*.88+per90(row,"Sh")*.03,2),"xAPer90":round(assists*.82+per90(row,"Crs")*.025,2),"tacklesPer90":tackles,"interceptionsPer90":interceptions,"progressiveCarriesPer90":round(1.2+seed+(.8 if role=="Forward" else 0),2),"progressivePassesPer90":round(2.5+assists*8+(1.2 if role=="Midfielder" else 0),2),"successfulDribblesPer90":round(.5+seed*.8,2),"foulsPer90":fouls,"pressuresPer90":round(8+tackles*2.4+fouls*1.6,2),"estimatedFields":True})
target.write_text("// Kaggle stats + latest Transfermarkt valuations; generated by import script.\nexport const kagglePlayers="+json.dumps(players,ensure_ascii=False,separators=(",",":"))+";\n",encoding="utf-8")
print(f"Wrote {len(players)} players; {matched} matched valuations ({matched/len(players):.1%})")
